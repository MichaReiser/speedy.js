function tspSync(points: number[]) {
    "use speedyjs";

    let currentX = points[0];
    let currentY = points[1];

    for (let i = 2; i < points.length - 2; i += 2) {
        let nearestIndex = i;
        let shortestDistance = euclideanDistance(currentX, currentY, points[nearestIndex], points[nearestIndex+1]);

        for (let j = nearestIndex + 2; j < points.length - 2; j += 2) {
            const distance = euclideanDistance(currentX, currentY, points[j], points[j+1]);

            if (distance < shortestDistance) {
                shortestDistance = distance;
                nearestIndex = j;
            }
        }

        currentX = points[nearestIndex];
        currentY = points[nearestIndex + 1];

        points[nearestIndex] = points[i];
        points[nearestIndex + 1] = points[i + 1];
        points[i] = currentX;
        points[i + 1] = currentY;
    }

    return computeCost(points);
}

function computeCost(tour: number[]) {
    "use speedyjs";
    let total = 0.0;

    for (let i = 0; i < tour.length - 4; i += 2) {
        total += euclideanDistance(tour[i], tour[i + 1], tour[i + 2], tour[i + 3]);
    }

    total += euclideanDistance(tour[tour.length - 2], tour[tour.length - 1], tour[0], tour[1]);

    return total;
}

function euclideanDistance(x1: number, y1: number, x2: number, y2: number): number {
    "use speedyjs";

    return Math.sqrt(Math.pow(x1 - x2, 2) + Math.pow(y1 - y2, 2));
}
